function caseInSwitch(val) {
  let answer = "";
  // Only change code below this line
  switch (val) {
    case 1:
      console.log((answer = "alpha"));
      break;
    case 2:
      console.log((answer = "beta"));
      break;
    case 3:
      console.log((answer = "gamma"));
      break;
    case 4:
      console.log((answer = "delta"));
      break;
  }

  // Only change code above this line
  return answer;
}

